package tokenizer;

public enum Token {
    COMMA,          // ','
    STAR,           // '*'
    SEMICOLON,      // ';'
    VARIABLE,       // name of variable
    TYPE,           // name of type
    END ,           // $
    LPAR,           // '['
    RPAR,           // ']'
    NUMB            // [0-9]
}
